                        <div class="card blog-details-card mb-30">
                            <h5 class="card-title">1) Lay Up Technique</h5>
                            <p class="card-text">Lay-ups are the backbone of any basketball team offence. As well as every player’s offensive skill set! Yet, the lay-up technique is one that basketballers generally spend less time on. Especially when compared to other shooting forms and techniques like jump-shooting or 3 point-shooting.</p>
                            <div class="card-img mb-3">
                                <img src="image/blog-details-inner-card-1.png" class="img-fluid">
                            </div>
                            <p class="card-text">Learning the fundamentals of lay-up techniques from an early age is essential in strengthening your offensive repertoire. The footwork involved in completing a lay-up is multifaceted. When performing a layup, you want to dribble close to the basket with your dominant hand. You want to get close enough to have easy access to the basket, but not so close that you are directly underneath it.</p>
                            <p class="card-text">Next, when approaching from the right side, step towards the basket with your right foot. This will allow you to gain distance. As well as, positioning yourself within easy shooting range. Following this, jump off your left foot for elevation and power, to bring you towards the basket for an easy completed layup.</p>
                            <p class="card-text">This is a small breakdown of the footwork behind a lay-up. And this is focused upon and taught more extensively at our ASC Basketball Camps. By attending an ASC basketball camp, your child will participate in multiple drills and games. Our professional coaches design them to improve your lay-up technique! In addition to, dribbling, passing, shooting and defensive techniques!</p>
                            <p class="card-text">Click Here for a parent’s information guide to an ASC Basketball Camp!</p>
                        </div>
                        
                        <div class="card blog-details-card mb-30">
                            <h5 class="card-title">2) Pivoting Technique</h5>
                            <p class="card-text">Pivoting in basketball is when a player keeps one foot in place while moving the other foot. Keeping one foot in place is necessary when a player stops dribbling the ball but wishes to re-position themselves for a pass or shot. Additionally, pivoting allows you to move around your defender without dribbling the basketball. Which creates different angles when looking to pass and moves you away from pressure from the defending player.</p>
                            <div class="card-img mb-3">
                                <img src="image/blog-details-inner-card-2.png" class="img-fluid">
                            </div>
                            <p class="card-text">The art of pivoting can take time to master for younger players. Also, learning when to use it and what footwork patterns work in different situations will take time too. So, at Australian Sports Camps we ensure that all participants undergo multifaceted pivoting drills during the camp duration! So, your child builds a foundation of understanding on pivoting. Which, they can take forwards into their basketball career.</p>
                            <p class="card-text">At ASC we strive to deliver an innovative program that covers all facets of the game of basketball! Designed to improve your skills and maximise enjoyment! So, whether you want to learn how to shoot 3’s like Steph Curry, or <a href="#" class="decoration-none color-blue">how to play basketball like Kobe Bryant</a>, Australian Sports Camps is a great opportunity to learn from industry professionals, and to have fun too!</p>
                            <p class="card-text">For more information about Australian Sports Camps, please <a href="#" class="decoration-none color-blue">click here!</a></p>
                        </div>
                        
                        <div class="card blog-details-card mb-30">
                            <h5 class="card-title">3) Jump Stops</h5>
                            <p class="card-text">Learning jump stops is an important milestone in a young basketballer’s development. In its most basic form, a jump stop is when a player comes to a stop landing simultaneously on both feet in a controlled stance. By landing with both feet at the same time, a jump stop can transition into a jump shot, layup or pivoting manoeuvre. This allows players to stop on a basketball court without travelling, in addition to a creating opportunity for an offensive shot.</p>
                            <div class="card-img mb-3">
                                <img src="image/blog-details-inner-card-3.png" class="img-fluid">
                            </div>
                            <p class="card-text">Jump stops are essential footwork fundamental for all players, both new and experienced. Just watch this attached video <a href="#" class="decoration-none color-blue"> here</a> and see how jump stops can impact a game differently! And therefore, why jump stops are also an essential element of our basketball programs. We adjust our camp drills depending on the age and skill set of our participant groupings. So your kids don’t need to worry about participating in drills beyond their current skill set. Therefore, everyone will be able to get the most out of every drill! And learn at their own pace whilst grasping all the basics necessary to improve their technique!</p>
                            <p class="card-text">An example of this would be that younger players will participate in drills designed to teach how to jump stop correctly. And more experienced players will undergo drills designed to teach players how to use jump stops, and say, jump shots effectively!</p>
